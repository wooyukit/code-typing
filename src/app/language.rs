//! Language definitions: keyword/type tables, comment + token rules, indentation,
//! and the sample set for each supported language.
//!
//! The syntax highlighter (`syntax::highlight`) and the game state are both driven
//! by `LanguageSpec`, so adding a language is almost entirely data: append a sample
//! module, define a `LanguageSpec`, and add it to `Language` + `ALL`.

use super::samples;

/// How a single quote `'` should be tokenized for a language.
#[derive(Clone, Copy, PartialEq, Eq)]
pub enum SingleQuote {
    /// `'a'` is a character/rune literal (C, C++, Java, Kotlin, Go, Rust).
    Char,
    /// `'...'` is a string literal (JavaScript, TypeScript, Python).
    Str,
    /// Single quotes are not string/char delimiters (Swift).
    None,
}

/// Everything the highlighter and game logic need to support one language.
pub struct LanguageSpec {
    pub name: &'static str,
    /// A short emoji shown beside the language in the menu and title.
    pub emoji: &'static str,
    pub keywords: &'static [&'static str],
    pub types: &'static [&'static str],
    /// Line-comment prefix, e.g. `//` or `#`.
    pub line_comment: &'static str,
    /// Block-comment delimiters, e.g. `("/*", "*/")`. `None` if unsupported.
    pub block_comment: Option<(&'static str, &'static str)>,
    pub single_quote: SingleQuote,
    /// Backtick template strings (JavaScript/TypeScript).
    pub backtick_string: bool,
    /// Rust lifetimes (`'a`).
    pub lifetimes: bool,
    /// Rust `ident!` macros.
    pub macros: bool,
    /// Rust `#[attribute]` syntax.
    pub rust_attributes: bool,
    /// C/C++ preprocessor directives (`#include`, `#define`).
    pub preprocessor: bool,
    /// Decorators / annotations (`@Override`, `@decorator`).
    pub decorators: bool,
    /// Spaces inserted for a Tab keystroke.
    pub indent_spaces: usize,
    pub samples: &'static [(&'static str, &'static str)],
}

/// Supported languages, in menu order.
#[derive(Clone, Copy, PartialEq, Eq, Debug)]
pub enum Language {
    Rust,
    Python,
    JavaScript,
    TypeScript,
    Go,
    Java,
    Kotlin,
    Swift,
    C,
    Cpp,
}

/// Languages in the order they appear in the selection menu.
pub const ALL: &[Language] = &[
    Language::Rust,
    Language::Python,
    Language::JavaScript,
    Language::TypeScript,
    Language::Go,
    Language::Java,
    Language::Kotlin,
    Language::Swift,
    Language::C,
    Language::Cpp,
];

impl Language {
    /// The static spec backing this language.
    pub fn spec(self) -> &'static LanguageSpec {
        match self {
            Language::Rust => &RUST,
            Language::Python => &PYTHON,
            Language::JavaScript => &JAVASCRIPT,
            Language::TypeScript => &TYPESCRIPT,
            Language::Go => &GO,
            Language::Java => &JAVA,
            Language::Kotlin => &KOTLIN,
            Language::Swift => &SWIFT,
            Language::C => &C,
            Language::Cpp => &CPP,
        }
    }

    pub fn name(self) -> &'static str {
        self.spec().name
    }
}

